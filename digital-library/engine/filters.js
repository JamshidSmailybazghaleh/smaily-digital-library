"use strict";

/* ==========================================================================
   FILTERS
   ========================================================================== */

const SKE_FILTERS = {

    /* ----------------------------------------------------------------------
       Published
    ---------------------------------------------------------------------- */

    published() {

        return SKE_LIBRARY

            .getPublishedBooks();

    },



    /* ----------------------------------------------------------------------
       Featured
    ---------------------------------------------------------------------- */

    featured() {

        return SKE_LIBRARY

            .getFeaturedBooks();

    },



    /* ----------------------------------------------------------------------
       Foundational
    ---------------------------------------------------------------------- */

    foundational() {

        return SKE_LIBRARY

            .getFoundationalBooks();

    },



    /* ----------------------------------------------------------------------
       Category
    ---------------------------------------------------------------------- */

    category(name) {

        return SKE_LIBRARY

            .getPublishedBooks()

            .filter(book =>

                book.category === name

            );

    },



    /* ----------------------------------------------------------------------
       Language
    ---------------------------------------------------------------------- */

    language(name) {

        return SKE_LIBRARY

            .getPublishedBooks()

            .filter(book =>

                book.language === name

            );

    },



    /* ----------------------------------------------------------------------
       Author
    ---------------------------------------------------------------------- */

    author(name) {

        return SKE_LIBRARY

            .getPublishedBooks()

            .filter(book =>

                book.author === name

            );

    },



    /* ----------------------------------------------------------------------
       Status
    ---------------------------------------------------------------------- */

    status(name) {

        return SKE_LIBRARY

            .getPublishedBooks()

            .filter(book =>

                book.status === name

            );

    },



    /* ----------------------------------------------------------------------
       Type
    ---------------------------------------------------------------------- */

    type(name) {

        return SKE_LIBRARY

            .getPublishedBooks()

            .filter(book =>

                book.type === name

            );

    },



    /* ----------------------------------------------------------------------
       Custom Filter
    ---------------------------------------------------------------------- */

    where(callback) {

        return SKE_LIBRARY

            .getPublishedBooks()

            .filter(callback);

    },



    /* ----------------------------------------------------------------------
       Sort Title
    ---------------------------------------------------------------------- */

    sortTitle(list) {

        return [...list]

            .sort((a,b)=>

                a.title.localeCompare(b.title)

            );

    },



    /* ----------------------------------------------------------------------
       Sort Author
    ---------------------------------------------------------------------- */

    sortAuthor(list) {

        return [...list]

            .sort((a,b)=>

                a.author.localeCompare(b.author)

            );

    },



    /* ----------------------------------------------------------------------
       Sort Latest
    ---------------------------------------------------------------------- */

    latest(list) {

        return [...list].reverse();

    }

};

/* ==========================================================================
   EXPORT
   ========================================================================== */

window.SKE_FILTERS = SKE_FILTERS;
